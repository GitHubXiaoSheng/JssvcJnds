package com.app.trafficclient.adapter;

public class Fankui {

    private String title;
    private String tiem;

    public Fankui(String title, String tiem) {
        this.title = title;
        this.tiem = tiem;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTiem() {
        return tiem;
    }

    public void setTiem(String tiem) {
        this.tiem = tiem;
    }
}
